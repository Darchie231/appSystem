    private void oTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_oTableMouseClicked
        
        int rowIndex = oTable.getSelectedRow();
        
        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null, "Please select an Item! ");
        }else{
            try{
            dbConnector dbc = new dbConnector();
            TableModel tbl = oTable.getModel();
            ResultSet rs = dbc.getData("SELECT * FROM tbl_oreders WHERE o_id = '"+tbl.getValueAt(rowIndex, 0)+"' ");
            if(rs.next()){
                
                pid.setText(""+rs.getString("p_id"));
                oquant.setText(""+rs.getString("quantity"));
                
                add.setEnabled(false);
                checkadd = false;
                
                
            }   
        }catch(SQLException ex){
                System.out.println(""+ex);
            }
        }
        
    }//GEN-LAST:event_oTableMouseClicked

    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        
        int rowIndex = oTable.getSelectedRow();
        
        if(pid.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Please select first! ");
        }else{
            if(oquant.getText().isEmpty() ){
                JOptionPane.showMessageDialog(null, "All fields are required! ");
            }else{
                dbConnector dbc = new dbConnector();
                TableModel tbl = oTable.getModel();
                dbc.updateData("UPDATE tbl_oreders SET quantity = '"+oquant.getText()+"' WHERE o_id = '"+tbl.getValueAt(rowIndex, 0)+"' ");
                
                displayData();
                
                checkadd = true;
        
                pid.setText("");
                oquant.setText("");
            }
        }
        
    }//GEN-LAST:event_updateMouseClicked

