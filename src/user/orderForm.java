    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked

            Session sess = Session.getInstance();
            
            if (pid.getText().isEmpty() || pname.getText().isEmpty() || oquant.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "All fields are required!");
            } else {
            dbConnector dbc = new dbConnector();

            // Ensure you properly format your SQL query with the correct number of parameters
            String sql = "INSERT INTO tbl_oreders (u_id, p_id, quantity, date, status) "
                   + "VALUES ('" + sess.getUid() + "', '" + pid.getText() + "', '" + oquant.getText() + "', NOW(), 'Pending')";

        // Execute the query
        if (dbc.insertData(sql)) {
            JOptionPane.showMessageDialog(null, "Order Added!");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to add order.");
        }
    }

    }//GEN-LAST:event_addMouseClicked

