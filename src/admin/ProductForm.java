    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        
        if(pid.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Please select first! ");
        }else{
            if(pname.getText().isEmpty() || pprice.getText().isEmpty()  ){
            JOptionPane.showMessageDialog(null, "All fields are required! ");
            }else{
                dbConnector dbc = new dbConnector();
                dbc.updateData("UPDATE tbl_medicine SET p_name = '"+pname.getText()+"', p_price = '"+pprice.getText()+"', "
                        + "p_status = '"+pstat.getSelectedItem()+"' WHERE p_id = '"+pid.getText()+"' ");
                
                displayData();
                
                checkadd = true;
        
                pid.setText("");
                pname.setText("");
                pprice.setText("");
                pstat.setSelectedIndex(0);
            }
        }
    }//GEN-LAST:event_updateMouseClicked

