    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        
        if(checkadd){
            
            if(pname.getText().isEmpty() || pprice.getText().isEmpty()  ){
            JOptionPane.showMessageDialog(null, "All fields are required! ");
            }else{
                dbConnector dbc = new dbConnector();
                dbc.insertData("INSERT INTO tbl_medicine (p_name, p_price , p_status, p_image) VALUES ('"+pname.getText()+"', '"+pprice.getText()+"', '"+pstat.getSelectedItem()+"', '') ");
                JOptionPane.showMessageDialog(null, "Addded Successfully! ");
                
                displayData();
                
                checkadd = true;
        
                pid.setText("");
                pname.setText("");
                pprice.setText("");
                pstat.setSelectedIndex(0);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Clear the fields first! ");
        }
        
        
    }//GEN-LAST:event_addMouseClicked

